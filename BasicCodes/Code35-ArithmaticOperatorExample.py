# Example of Arithmatic Operator

a = int(input("Enter the number: "))
b = int(input("Enter the number: "))

# Addition
c = (a + b)
print("Sum is: ",c)

# Substraction
d = (a - b)
print("Difference is: ",d)

# Multiplication
e = (a * b)
print("Multiplication is: ",e)

# Division
f = (a / b)
print("Division is: ",f)

# Remainder
g = (a % b)
print("Remainder Division is: ",g)

# Exponent
h = (a ** b)
print("Exponent is: ",h)

# Floor Division
g = (a // b)
print("Floor Division is: ",g)
