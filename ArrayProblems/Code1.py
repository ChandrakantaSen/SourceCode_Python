# WAP to traverse the elements of an array
import array as arr
p = arr.array('i',[2,4,6,8])

print("First Element: ",p[0])
print("Second Element: ",p[1])
print("Third Element: ",p[2])
print("Fourth Element: ",p[3])

print("From Last First Element: ",p[-1])
print("From Last Second Element: ",p[-2])
print("From Last Third Element: ",p[-3])
print("From Last Fourth Element: ",p[-4])